<template>
  <div class="tab-control">
    <div v-for="(item, index) in titles" :key="index" :class="{active: index == currentIndex }"
         @click="getActiveIndex(index)" class="tab-control-item">
      <span>{{item}}</span>
    </div>
  </div>
</template>

<script>
    export default {
      name: "TabControl",
      data() {
        return {
          currentIndex: 0
        }
      },
      methods: {
        getActiveIndex(index){
          this.currentIndex = index;
          this.$emit('tabClick', index)
        }
      },
      props: {
          titles:{
            type: Array,
            default() {
              return []
            }
          }
      }
    }
</script>

<style scoped>
  .tab-control {
    display: flex;
    text-align: center;
    font-size: 16px;

  }
  .tab-control-item {
    flex: 1;
    height: 40px;
    line-height: 40px;
  }
  .active {
    color: var(--color-high-text);
  }
  .active span {
    border-bottom: 3px solid var(--color-tint);
    padding: 4px;
  }

</style>
